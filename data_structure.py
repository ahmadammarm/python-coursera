# dalam python ada juga yang namanya data structure
# data structure adalah struktur yang digunakan untuk menyimpan dan mengorganisir data

# ada 2 jenis data structure dalam python:
# 1. Built-in data structure
# 2. User-defined

# 1. Built-in data structure
# built-in data structure adalah struktur data yang sudah disediakan oleh python dan bisa langsung digunakan
# ada beberapa jenis built-in data structure dalam python:
# - List
# - Tuple
# - Set
# - Dictionary

# 2. User-defined data structure
# user-defined data structure adalah struktur data yang bisa dibuat oleh programmer sendiri (dibuat dari built-in data structure)
# ada beberapa jenis user-defined data structure dalam python:
# - Stack
# - Queue
# - Linked List
# - Tree
# - Graph
# - Hashmap
# - Heap

# kita akan belajar tentang built-in data structure terlebih dahulu